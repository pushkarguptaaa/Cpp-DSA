#include <iostream>
using namespace std;
int main(){
    int a;
    a = 14;
    cout<<"sixe of int: "<<sizeof(a)<<endl;
    float b;
    cout<<"size of float: "<<sizeof(b)<<endl;
    char c;
    cout<<"size of char: "<<sizeof(c)<<endl;
    bool d;
    cout<<"size of bool: "<<sizeof(d)<<endl;

    // type modifiers
    short int e;
    cout<<"size of si: "<<sizeof(e)<<endl;
    long int f;
    cout<<"size of li: "<<sizeof(f)<<endl;

}